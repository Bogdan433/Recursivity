// o metoda care returneaza suma a doua numere primite
// o metoda care returneaza difererenta a doua numere primite
// o metoda care returneaza prod a doua numere primite
//o metoda care returneaza impartirea a doua numere primite
public class Calculator {
    public int getSum(int a,int b){
        return a + b;
    }
    public int getDif(int a,int b){
        return a - b;
    }
    public int getProd(int a,int b){
        return a * b;
    }
    public int getImp(int a,int b){
        return a / b;
    }

}
